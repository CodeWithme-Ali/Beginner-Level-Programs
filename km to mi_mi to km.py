# program to Convert kilometers to miles and vice versa.

print("\nConverting miles to kilometers...")
a = int(input("Enter value of miles please: "))
km = (a*1.6)
print(f"Answer is {km} kilometers")

print("\nConverting kilometers to miles...")
b = int(input("Enter value of kilometers please: "))
mi = (b/1.6)
print(f"Answer is {mi} miles")
print("\nThank You!")