# Program to calculate area and perimeter of a rectangle.


# Area of Rectangle...

print("Please Enter Values Below to Calculate The Area of a Rectangle.")
L = int(input("Enter Length of a rectangle in cm: "))
W = int(input("Enter Width of a rectangle in cm: "))
print(f"Area of a rectangle in cm is: {L*W}")


# Perimeter of Rectangle...

print("Please Enter Values Below to Calculate The Perimeter of a Rectangle.")
L = int(input("Enter Length of a rectangle in cm: "))
W = int(input("Enter Width of a rectangle in cm: "))
print(f"Perimeter of a rectangle in cm is: {(L+W)*2}")
